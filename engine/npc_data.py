def _make_npc(name, identity, official_title, tags_str, party, faction):
    """构造NPC字典（含官职和派系字段）"""
    tags = [t.strip() for t in tags_str.split("、") if t.strip()]
    power_map = {
        "皇帝":"权倾","太师":"权倾","枢密使":"朝堂","太宰":"朝堂","少宰":"朝堂",
        "西军统帅":"朝堂","经略使":"地方","知州":"地方","将领":"小吏",
        "太学生":"微末","内侍":"小吏","道士":"微末","商人":"微末","乡绅":"微末",
    }
    power = "小吏"
    for keyword, p in power_map.items():
        if keyword in identity:
            power = p
            break

    return {
        "name": name,
        "npc_identity": identity,
        "npc_official_title": official_title,
        "npc_party": party,
        "npc_faction": faction,
        "npc_power": power,
        "tags": json.dumps(tags, ensure_ascii=False),
        "note": f"{faction}地界之{identity}。{tags_str}",
        "is_active": "true",
        "months_no_interact": 0,
    }


def get_all_npcs():
    """获取所有NPC"""
    all_npcs = []
    for key, (label, npc_list) in ALL_NPC_GROUPS.items():
        faction = FACTION_MAP[key]
        for npc_tuple in npc_list:
            npc_dict = _make_npc(*npc_tuple, faction=faction)
            all_npcs.append(npc_dict)
    return all_npcs


def get_npcs_by_faction(faction):
    """按势力筛选NPC"""
    return [n for n in get_all_npcs() if n["npc_faction"] == faction]


def get_npc_count():
    """统计"""
    groups = {}
    for key, (label, npc_list) in ALL_NPC_GROUPS.items():
        groups[label] = len(npc_list)
    all_n = get_all_npcs()
    return {"groups": groups, "total": len(all_n)}


def get_npcs_by_party(party):
    """按派系筛选NPC"""
    return [n for n in get_all_npcs() if n.get("npc_party", "").startswith(party)]


