def get_event_for_month(current_time, already_triggered=None):
    """
    根据当前月份返回默认触发事件
    already_triggered: 已触发的事件集合
    """
    if already_triggered is None:
        already_triggered = set()

    for month, desc, etype, default in HISTORICAL_EVENTS:
        if month == current_time and default and month not in already_triggered:
            return {"month": month, "desc": desc, "type": etype}

    return None


def get_all_default_events():
    """获取所有默认触发事件列表"""
    return [(m, d, t) for m, d, t, default in HISTORICAL_EVENTS if default]
